# DBTITLE 1,Step 3 - M2M Token Test
# MAGIC %md
# MAGIC ## Step 3 — M2M Client Credentials Test
# MAGIC
# MAGIC **PASS means:** The App Connection exists and the Client Secret is correct.
# MAGIC
# MAGIC **PASS does NOT prove:** Full U2M flow will work. Also verify:
# MAGIC - Exact Glean callback URL is set as redirect URL in the App Connection
# MAGIC - `offline_access` scope is enabled
# MAGIC
# MAGIC **Common failures:**
# MAGIC - `invalid_client` — wrong Client ID or Secret
# MAGIC - `OAuth application ... not available in Databricks account` — Client ID is a service principal secret, not an App Connection. Go to Account Console → Settings → App Connections and create one.

# COMMAND ----------

TOKEN_URL = f"{WORKSPACE_HOST}/oidc/v1/token"

if not CLIENT_ID or not CLIENT_SECRET:
    print("[SKIP] CLIENT_ID or CLIENT_SECRET is empty. Fill in the widgets and re-run from Cell 2.")
else:
    try:
        token_resp = requests.post(
            TOKEN_URL,
            data={"grant_type": "client_credentials", "scope": "all-apis"},
            auth=(CLIENT_ID, CLIENT_SECRET),
            timeout=10,
        )
        if token_resp.status_code == 200:
            token_data = token_resp.json()
            print("[PASS] Client credentials are valid.")
            print(f"  Token type : {token_data.get('token_type')}")
            print(f"  Expires in : {token_data.get('expires_in')} seconds")
            print(f"  Scope      : {token_data.get('scope')}")
        else:
            print(f"[FAIL] HTTP {token_resp.status_code}")
            try:
                err = token_resp.json()
                print(f"  error             : {err.get('error')}")
                print(f"  error_description : {err.get('error_description')}")
            except Exception:
                print(f"  Raw response: {token_resp.text}")
    except Exception as e:
        token_resp = None
        print(f"[FAIL] Request error: {e}")

