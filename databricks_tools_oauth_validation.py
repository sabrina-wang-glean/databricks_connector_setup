# MAGIC %md
# MAGIC ## Step 5 (Optional) — SQL Statement API Smoke Test
# MAGIC
# MAGIC Verifies the token works for data access by running a query against `samples.tpch.orders`.
# MAGIC Available in **every** Databricks workspace with no setup required.
# MAGIC
# MAGIC - Auto-discovers a running warehouse if `WAREHOUSE_ID` is blank
# MAGIC - This is the closest programmatic proxy for what Glean's SQL tool will do via U2M

# COMMAND ----------

import time

if not creds_ok:
    print("[SKIP] Token test failed in Step 3 — cannot run smoke test.")
else:
    access_token = token_data["access_token"]
    headers = {"Authorization": f"Bearer {access_token}"}

    warehouse_id = WAREHOUSE_ID
    if not warehouse_id:
        print("No WAREHOUSE_ID provided — discovering available warehouses...\n")
        wh_resp = requests.get(f"{WORKSPACE_HOST}/api/2.0/sql/warehouses", headers=headers, timeout=10)
        if wh_resp.status_code == 200:
            warehouses = wh_resp.json().get("warehouses", [])
            if warehouses:
                running = [w for w in warehouses if w.get("state") == "RUNNING"]
                chosen = running[0] if running else warehouses[0]
                warehouse_id = chosen["id"]
                print(f"Found {len(warehouses)} warehouse(s). Using: {chosen['name']} (ID: {warehouse_id}, State: {chosen.get('state')})")
            else:
                print("[SKIP] No SQL warehouses found. Create one at SQL > SQL Warehouses > Create.")
                warehouse_id = None
        else:
            print(f"[FAIL] Could not list warehouses: HTTP {wh_resp.status_code}")
            warehouse_id = None
    else:
        print(f"Using provided WAREHOUSE_ID: {warehouse_id}")

# COMMAND ----------

SQL_TEST_QUERY = """
SELECT
  o_orderstatus AS status,
  COUNT(*) AS order_count,
  ROUND(SUM(o_totalprice), 2) AS total_value
FROM samples.tpch.orders
GROUP BY o_orderstatus
ORDER BY total_value DESC
LIMIT 5
"""

if not creds_ok:
    print("[SKIP] No valid token.")
elif not warehouse_id:
    print("[SKIP] No warehouse available.")
else:
    print(f"Executing query against samples.tpch.orders...\n")
    stmt_resp = requests.post(
        f"{WORKSPACE_HOST}/api/2.0/sql/statements",
        headers=headers,
        json={"warehouse_id": warehouse_id, "statement": SQL_TEST_QUERY, "wait_timeout": "30s", "disposition": "INLINE"},
        timeout=60,
    )
    if stmt_resp.status_code == 200:
        result = stmt_resp.json()
        state = result.get("status", {}).get("state")
        if state == "SUCCEEDED":
            print("[PASS] SQL Statement API test succeeded!\n")
            columns = [c["name"] for c in result.get("manifest", {}).get("schema", {}).get("columns", [])]
            rows = result.get("result", {}).get("data_array", [])
            print(f"  {'  |  '.join(columns)}")
            for row in rows:
                print(f"  {'  |  '.join(str(v) for v in row)}")
        else:
            print(f"[FAIL] Statement state: {state}")
            print(f"  {result.get('status', {}).get('error', {}).get('message', 'unknown error')}")
    else:
        print(f"[FAIL] HTTP {stmt_resp.status_code}: {stmt_resp.text[:300]}")

